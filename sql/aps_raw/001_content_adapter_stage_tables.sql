-- APS markdown-note adapter stage tables.
--
-- These APS_RAW tables are keyed by adapter-produced business keys plus
-- INGESTION_RUN_ID. They intentionally carry both deterministic IDs and parent
-- business keys so merges can resolve relationships through business keys rather
-- than trusting Snowflake metadata constraints.

CREATE SCHEMA IF NOT EXISTS APS_RAW;

