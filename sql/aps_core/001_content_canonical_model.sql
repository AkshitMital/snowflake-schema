);

COMMENT ON TABLE APS_CORE.CONTENT_SCOPES IS
    'Intended scope of the content, such as strategy, portfolio, investment, multi-investment, unknown, or mixed.';
COMMENT ON COLUMN APS_CORE.CONTENT_SCOPES.SCOPE_TYPE IS
    'Scope classification, for example STRATEGY, PORTFOLIO, INVESTMENT, MULTI_INVESTMENT, MARKET, FIRM, UNKNOWN, MIXED.';

