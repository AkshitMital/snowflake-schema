    CONTENT_VERSION_ID VARCHAR,
    SCOPE_TYPE VARCHAR NOT NULL,
    SCOPE_LABEL VARCHAR,
    SCOPE_ENTITY_TYPE VARCHAR,
    SCOPE_ENTITY_ID VARCHAR,
    SCOPE_ENTITY_NAME VARCHAR,
    IS_PRIMARY_SCOPE BOOLEAN NOT NULL DEFAULT FALSE,
    CONFIDENCE FLOAT,
    EVIDENCE_TEXT VARCHAR,
    EVIDENCE VARIANT,
    CLASSIFICATION_METHOD VARCHAR,
    REVIEW_STATUS VARCHAR NOT NULL DEFAULT 'UNREVIEWED',
    METADATA VARIANT,
    CREATED_AT TIMESTAMP_NTZ NOT NULL DEFAULT CURRENT_TIMESTAMP(),
    UPDATED_AT TIMESTAMP_NTZ NOT NULL DEFAULT CURRENT_TIMESTAMP(),
    CONSTRAINT PK_CONTENT_SCOPES PRIMARY KEY (CONTENT_SCOPE_ID),
