COMMENT ON COLUMN APS_CORE.CONTENT_ENTITY_LINKS.REVIEW_STATUS IS
    'Resolution review state, for example AUTO_ACCEPTED, APPROVED, NEEDS_REVIEW, REJECTED, UNREVIEWED.';

