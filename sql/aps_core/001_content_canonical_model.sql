);

COMMENT ON TABLE APS_CORE.CONTENT_VERSIONS IS
    'Exports, re-exports, revisions, ingestion runs, fingerprints, duplicate detection, and version lineage for content items.';
COMMENT ON COLUMN APS_CORE.CONTENT_VERSIONS.CONTENT_VERSION_KEY IS
    'Adapter-produced merge key for one normalized version. Recommended shape: CONTENT_ITEM_KEY + content fingerprint, or CONTENT_ITEM_KEY + source version id when the source has immutable versions.';
COMMENT ON COLUMN APS_CORE.CONTENT_VERSIONS.VERSION_REASON IS
    'Why this version exists, for example INITIAL_INGEST, SOURCE_REVISION, RE_EXPORT_IDENTICAL, MOVED_FILE, ADAPTER_REPROCESS, DUPLICATE_DETECTED.';
COMMENT ON COLUMN APS_CORE.CONTENT_VERSIONS.CONTENT_FINGERPRINT IS
    'Composite fingerprint across normalized body and assets. Prefer this over URL-only dedupe.';
COMMENT ON COLUMN APS_CORE.CONTENT_VERSIONS.VERSION_STATUS IS
    'Version lifecycle state, for example CANDIDATE, CURRENT, SUPERSEDED, DUPLICATE, RETRACTED, FAILED_EXTRACTION.';
COMMENT ON COLUMN APS_CORE.CONTENT_VERSIONS.IS_CURRENT IS
    'Denormalized current flag maintained by ingestion. APS_CORE.CONTENT_ITEM_CURRENT_VERSION is the deterministic pointer used by production views.';

CREATE TABLE IF NOT EXISTS APS_CORE.CONTENT_ITEM_CURRENT_VERSION (
    CONTENT_ID VARCHAR NOT NULL,
    CURRENT_CONTENT_VERSION_ID VARCHAR NOT NULL,
    CURRENT_CONTENT_VERSION_KEY VARCHAR NOT NULL,
    SELECTED_AT TIMESTAMP_NTZ NOT NULL DEFAULT CURRENT_TIMESTAMP(),
    SELECTION_REASON VARCHAR NOT NULL,
    UPDATED_AT TIMESTAMP_NTZ NOT NULL DEFAULT CURRENT_TIMESTAMP(),
    CONSTRAINT PK_CONTENT_ITEM_CURRENT_VERSION PRIMARY KEY (CONTENT_ID),
    CONSTRAINT FK_CURRENT_VERSION_ITEM FOREIGN KEY (CONTENT_ID)
        REFERENCES APS_CORE.CONTENT_ITEMS (CONTENT_ID),
    CONSTRAINT FK_CURRENT_VERSION_VERSION FOREIGN KEY (CURRENT_CONTENT_VERSION_ID)
        REFERENCES APS_CORE.CONTENT_VERSIONS (CONTENT_VERSION_ID)
);

COMMENT ON TABLE APS_CORE.CONTENT_ITEM_CURRENT_VERSION IS
    'One-row-per-content-item pointer to the current version. Use this table rather than trusting IS_CURRENT alone.';
COMMENT ON COLUMN APS_CORE.CONTENT_ITEM_CURRENT_VERSION.SELECTION_REASON IS
    'Why this version became current, for example INITIAL_INGEST, NEW_SOURCE_REVISION, RECONCILED_MOVE, MANUAL_OVERRIDE.';

