-- APS generic content canonical model v2.
--
-- APS_RAW owns ingestion files, raw payloads, and lineage. APS_CORE owns normalized
-- source truth. These tables preserve logical content objects without forcing broad
-- notes into investment-shaped records too early.
--
-- Snowflake standard-table primary, foreign, and unique constraints are metadata,
-- not full integrity enforcement. The business keys and health views in this repo
-- are part of the ingestion contract, not optional documentation.

CREATE SCHEMA IF NOT EXISTS APS_CORE;

CREATE TABLE IF NOT EXISTS APS_CORE.CONTENT_ITEMS (
    CONTENT_ID VARCHAR NOT NULL,
    CONTENT_ITEM_KEY VARCHAR NOT NULL,
    LOGICAL_SOURCE_KEY VARCHAR NOT NULL,
    ITEM_IDENTITY_METHOD VARCHAR NOT NULL,
    SOURCE_SYSTEM VARCHAR NOT NULL,
    SOURCE_OBJECT_ID VARCHAR,
    SOURCE_CONTAINER VARCHAR,
    SOURCE_PATH VARCHAR,
    SOURCE_URI VARCHAR,
    SOURCE_URI_NORMALIZED VARCHAR,
    SOURCE_REFERENCE VARCHAR,
    SOURCE_TYPE VARCHAR NOT NULL,
    SOURCE_SUBTYPE VARCHAR,
    SOURCE_TITLE VARCHAR,
    SOURCE_PROVENANCE VARIANT,
    SOURCE_CREATED_AT TIMESTAMP_NTZ,
    SOURCE_MODIFIED_AT TIMESTAMP_NTZ,
    FIRST_SEEN_AT TIMESTAMP_NTZ NOT NULL DEFAULT CURRENT_TIMESTAMP(),
    LAST_SEEN_AT TIMESTAMP_NTZ NOT NULL DEFAULT CURRENT_TIMESTAMP(),
    CANONICAL_CONTENT_HASH VARCHAR,
    DUPLICATE_OF_CONTENT_ID VARCHAR,
    REVIEW_STATUS VARCHAR NOT NULL DEFAULT 'UNREVIEWED',
    STATUS_REASON VARCHAR,
    IS_ACTIVE BOOLEAN NOT NULL DEFAULT TRUE,
    METADATA VARIANT,
    CREATED_AT TIMESTAMP_NTZ NOT NULL DEFAULT CURRENT_TIMESTAMP(),
    UPDATED_AT TIMESTAMP_NTZ NOT NULL DEFAULT CURRENT_TIMESTAMP(),
    CONSTRAINT PK_CONTENT_ITEMS PRIMARY KEY (CONTENT_ID),
    CONSTRAINT UK_CONTENT_ITEMS_ITEM_KEY UNIQUE (CONTENT_ITEM_KEY)
);

COMMENT ON TABLE APS_CORE.CONTENT_ITEMS IS
    'One logical source object such as a PM note, strategy note, portfolio update, markdown export, webcast, or future source.';
COMMENT ON COLUMN APS_CORE.CONTENT_ITEMS.CONTENT_ITEM_KEY IS
    'Adapter-produced merge key for the logical content item. Prefer source object id. Fall back to normalized source URI/path. Do not use raw URL alone when a stable object id exists.';
COMMENT ON COLUMN APS_CORE.CONTENT_ITEMS.LOGICAL_SOURCE_KEY IS
    'Human-readable stable source identity used for traceability. Usually same input material as CONTENT_ITEM_KEY before hashing or namespacing.';
COMMENT ON COLUMN APS_CORE.CONTENT_ITEMS.ITEM_IDENTITY_METHOD IS
    'How the adapter chose CONTENT_ITEM_KEY, for example SOURCE_OBJECT_ID, NORMALIZED_URI, CONTAINER_PATH, CONTENT_HASH_RECONCILED.';
COMMENT ON COLUMN APS_CORE.CONTENT_ITEMS.CANONICAL_CONTENT_HASH IS
    'Normalized content fingerprint used for duplicate detection across source systems, moved files, and re-exports. This is not the primary item identity when source object ids exist.';
COMMENT ON COLUMN APS_CORE.CONTENT_ITEMS.DUPLICATE_OF_CONTENT_ID IS
    'Canonical item this item duplicates when duplicate detection finds separate item keys for the same logical content.';
COMMENT ON COLUMN APS_CORE.CONTENT_ITEMS.REVIEW_STATUS IS
    'Human or workflow review state, for example UNREVIEWED, APPROVED, NEEDS_REVIEW, REJECTED, DUPLICATE, SUPERSEDED.';

CREATE TABLE IF NOT EXISTS APS_CORE.CONTENT_VERSIONS (
    CONTENT_VERSION_ID VARCHAR NOT NULL,
    CONTENT_ID VARCHAR NOT NULL,
    CONTENT_VERSION_KEY VARCHAR NOT NULL,
    VERSION_NUMBER NUMBER(38, 0) NOT NULL,
    VERSION_REASON VARCHAR NOT NULL DEFAULT 'INITIAL_INGEST',
    INGESTION_RUN_ID VARCHAR,
    RAW_RECORD_ID VARCHAR,
    RAW_PAYLOAD_LOCATION VARCHAR,
    NORMALIZED_PAYLOAD_LOCATION VARCHAR,
    SOURCE_EXPORT_ID VARCHAR,
    SOURCE_VERSION_ID VARCHAR,
    SOURCE_EXPORTED_AT TIMESTAMP_NTZ,
    SOURCE_MODIFIED_AT TIMESTAMP_NTZ,
    ADAPTER_NAME VARCHAR,
    ADAPTER_VERSION VARCHAR,
    SOURCE_FILE_HASH VARCHAR,
    BODY_HASH VARCHAR,
    ASSET_FINGERPRINT VARCHAR,
    CONTENT_FINGERPRINT VARCHAR,
    PREVIOUS_CONTENT_VERSION_ID VARCHAR,
    SUPERSEDED_BY_CONTENT_VERSION_ID VARCHAR,
    DUPLICATE_OF_CONTENT_VERSION_ID VARCHAR,
    DUPLICATE_REASON VARCHAR,
    VERSION_STATUS VARCHAR NOT NULL DEFAULT 'CANDIDATE',
    IS_CURRENT BOOLEAN NOT NULL DEFAULT FALSE,
    CURRENT_FROM_AT TIMESTAMP_NTZ,
    CURRENT_TO_AT TIMESTAMP_NTZ,
    METADATA VARIANT,
    CREATED_AT TIMESTAMP_NTZ NOT NULL DEFAULT CURRENT_TIMESTAMP(),
    UPDATED_AT TIMESTAMP_NTZ NOT NULL DEFAULT CURRENT_TIMESTAMP(),
    CONSTRAINT PK_CONTENT_VERSIONS PRIMARY KEY (CONTENT_VERSION_ID),
    CONSTRAINT UK_CONTENT_VERSIONS_VERSION_KEY UNIQUE (CONTENT_VERSION_KEY),
