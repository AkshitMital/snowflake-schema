    'Derived portfolio/fund-facing notes from generic content using approved portfolio or fund entity links.';

CREATE OR REPLACE VIEW APS_CORE.STRATEGY_NOTES_FROM_CONTENT AS
WITH strategy_links AS (
    SELECT
        cel.CONTENT_ENTITY_LINK_ID,
        cel.CONTENT_ID,
        ccv.CONTENT_VERSION_ID,
        cel.CONTENT_BODY_ID,
        cel.ENTITY_ID AS STRATEGY_ID,
        cel.ENTITY_NAME AS STRATEGY_NAME,
        cel.ENTITY_TYPE AS STRATEGY_ENTITY_TYPE,
        cel.CONFIDENCE AS ENTITY_LINK_CONFIDENCE,
        cel.EVIDENCE_TEXT AS ENTITY_LINK_EVIDENCE,
        cel.REVIEW_STATUS AS ENTITY_LINK_REVIEW_STATUS,
        ctx.STRATEGY_DERIVATION_REQUIRES_BODY_ANCHOR
    FROM APS_CORE.CONTENT_ENTITY_LINKS cel
    JOIN APS_CORE.CONTENT_CURRENT_VERSIONS ccv
        ON ccv.CONTENT_ID = cel.CONTENT_ID
       AND (cel.CONTENT_VERSION_ID = ccv.CONTENT_VERSION_ID OR cel.CONTENT_VERSION_ID IS NULL)
    JOIN APS_CORE.CONTENT_ENTITY_LINK_DERIVATION_CONTEXT ctx
        ON ctx.CONTENT_ID = ccv.CONTENT_ID
       AND ctx.CONTENT_VERSION_ID = ccv.CONTENT_VERSION_ID
    WHERE UPPER(cel.ENTITY_TYPE) IN ('STRATEGY', 'TOPIC')
      AND cel.ENTITY_NAME IS NOT NULL
      AND COALESCE(cel.CONFIDENCE, 0) >= 0.80
      AND UPPER(cel.REVIEW_STATUS) IN ('AUTO_ACCEPTED', 'APPROVED')
      AND (
          cel.CONTENT_BODY_ID IS NOT NULL
          OR (
              NOT ctx.STRATEGY_DERIVATION_REQUIRES_BODY_ANCHOR
              AND (
                  cel.IS_PRIMARY_SUBJECT
                  OR UPPER(cel.LINK_ROLE) IN ('PRIMARY_SUBJECT', 'DOCUMENT_SUBJECT')
              )
          )
      )
),
strategy_scopes_without_links AS (
    SELECT
        cs.CONTENT_SCOPE_ID,
        cs.CONTENT_ID,
        ccv.CONTENT_VERSION_ID,
        cs.SCOPE_ENTITY_ID AS STRATEGY_ID,
        COALESCE(cs.SCOPE_ENTITY_NAME, cs.SCOPE_LABEL) AS STRATEGY_NAME,
        cs.CONFIDENCE AS ENTITY_LINK_CONFIDENCE,
        cs.REVIEW_STATUS AS ENTITY_LINK_REVIEW_STATUS
    FROM APS_CORE.CONTENT_SCOPES cs
    JOIN APS_CORE.CONTENT_CURRENT_VERSIONS ccv
        ON ccv.CONTENT_ID = cs.CONTENT_ID
       AND (cs.CONTENT_VERSION_ID = ccv.CONTENT_VERSION_ID OR cs.CONTENT_VERSION_ID IS NULL)
    WHERE UPPER(cs.SCOPE_TYPE) = 'STRATEGY'
      AND COALESCE(cs.SCOPE_ENTITY_NAME, cs.SCOPE_LABEL) IS NOT NULL
      AND COALESCE(cs.CONFIDENCE, 0) >= 0.80
      AND UPPER(cs.REVIEW_STATUS) IN ('AUTO_ACCEPTED', 'APPROVED')
      AND NOT EXISTS (
          SELECT 1
          FROM strategy_links sl
          WHERE sl.CONTENT_ID = cs.CONTENT_ID
            AND sl.CONTENT_VERSION_ID = ccv.CONTENT_VERSION_ID
      )
)
SELECT
    sl.STRATEGY_ID,
    sl.STRATEGY_NAME,
    sl.STRATEGY_ENTITY_TYPE,
    sod.OPERATIONAL_DATE AS EFFECTIVE_DATE,
    ccv.SOURCE_TYPE,
    ccv.SOURCE_URI AS SOURCE_URL,
    IFF(UPPER(ccv.SOURCE_SYSTEM) = 'SHAREPOINT', ccv.SOURCE_URI, NULL) AS SOURCE_SHAREPOINT,
    ccv.SOURCE_REFERENCE,
    LEFT(REGEXP_REPLACE(COALESCE(lb.BODY_TEXT, pb.BODY_TEXT), '[[:space:]]+', ' '), 8000) AS SUMMARY,
    COALESCE(lb.BODY_TEXT, pb.BODY_TEXT) AS NOTE_TEXT,
    ccv.CONTENT_ID AS SOURCE_CONTENT_ID,
    ccv.CONTENT_VERSION_ID AS SOURCE_CONTENT_VERSION_ID,
    sl.CONTENT_ENTITY_LINK_ID AS SOURCE_ENTITY_LINK_ID,
    COALESCE(lb.CONTENT_BODY_ID, pb.CONTENT_BODY_ID) AS SOURCE_CONTENT_BODY_ID,
    sl.ENTITY_LINK_CONFIDENCE,
    sl.ENTITY_LINK_REVIEW_STATUS,
    sod.CONTENT_DATE_ID AS SOURCE_CONTENT_DATE_ID,
    sod.OPERATIONAL_DATE_TYPE AS EFFECTIVE_DATE_SOURCE_TYPE,
    sod.OPERATIONAL_DATE_CONFIDENCE AS EFFECTIVE_DATE_CONFIDENCE,
    CASE
        WHEN sod.OPERATIONAL_DATE IS NULL THEN 'DERIVED_STRATEGY_NO_OPERATIONAL_DATE'
        WHEN sl.CONTENT_BODY_ID IS NULL THEN 'DERIVED_STRATEGY_DOCUMENT_LEVEL'
        ELSE 'DERIVED_STRATEGY_BODY_ANCHORED'
    END AS DERIVATION_STATUS,
    'strategy_link_confidence_0_80_anchor_v2' AS DERIVATION_RULE_VERSION,
    CURRENT_TIMESTAMP() AS VIEW_EVALUATED_AT
FROM strategy_links sl
JOIN APS_CORE.CONTENT_CURRENT_VERSIONS ccv
    ON ccv.CONTENT_ID = sl.CONTENT_ID
   AND ccv.CONTENT_VERSION_ID = sl.CONTENT_VERSION_ID
LEFT JOIN APS_CORE.CONTENT_BODIES lb
    ON lb.CONTENT_BODY_ID = sl.CONTENT_BODY_ID
   AND lb.CONTENT_VERSION_ID = ccv.CONTENT_VERSION_ID
LEFT JOIN APS_CORE.CONTENT_PRIMARY_BODIES pb
    ON pb.CONTENT_VERSION_ID = ccv.CONTENT_VERSION_ID
LEFT JOIN APS_CORE.CONTENT_SELECTED_OPERATIONAL_DATES sod
    ON sod.CONTENT_ID = ccv.CONTENT_ID
   AND sod.CONTENT_VERSION_ID = ccv.CONTENT_VERSION_ID
UNION ALL
SELECT
    sswl.STRATEGY_ID,
    sswl.STRATEGY_NAME,
    'STRATEGY_SCOPE' AS STRATEGY_ENTITY_TYPE,
    sod.OPERATIONAL_DATE AS EFFECTIVE_DATE,
    ccv.SOURCE_TYPE,
    ccv.SOURCE_URI AS SOURCE_URL,
    IFF(UPPER(ccv.SOURCE_SYSTEM) = 'SHAREPOINT', ccv.SOURCE_URI, NULL) AS SOURCE_SHAREPOINT,
    ccv.SOURCE_REFERENCE,
    LEFT(REGEXP_REPLACE(pb.BODY_TEXT, '[[:space:]]+', ' '), 8000) AS SUMMARY,
    pb.BODY_TEXT AS NOTE_TEXT,
    ccv.CONTENT_ID AS SOURCE_CONTENT_ID,
    ccv.CONTENT_VERSION_ID AS SOURCE_CONTENT_VERSION_ID,
    NULL AS SOURCE_ENTITY_LINK_ID,
    pb.CONTENT_BODY_ID AS SOURCE_CONTENT_BODY_ID,
    sswl.ENTITY_LINK_CONFIDENCE,
    sswl.ENTITY_LINK_REVIEW_STATUS,
    sod.CONTENT_DATE_ID AS SOURCE_CONTENT_DATE_ID,
    sod.OPERATIONAL_DATE_TYPE AS EFFECTIVE_DATE_SOURCE_TYPE,
    sod.OPERATIONAL_DATE_CONFIDENCE AS EFFECTIVE_DATE_CONFIDENCE,
    'DERIVED_STRATEGY_SCOPE_DOCUMENT_LEVEL' AS DERIVATION_STATUS,
    'strategy_scope_confidence_0_80_v2' AS DERIVATION_RULE_VERSION,
    CURRENT_TIMESTAMP() AS VIEW_EVALUATED_AT
FROM strategy_scopes_without_links sswl
JOIN APS_CORE.CONTENT_CURRENT_VERSIONS ccv
    ON ccv.CONTENT_ID = sswl.CONTENT_ID
   AND ccv.CONTENT_VERSION_ID = sswl.CONTENT_VERSION_ID
LEFT JOIN APS_CORE.CONTENT_PRIMARY_BODIES pb
    ON pb.CONTENT_VERSION_ID = ccv.CONTENT_VERSION_ID
LEFT JOIN APS_CORE.CONTENT_SELECTED_OPERATIONAL_DATES sod
    ON sod.CONTENT_ID = ccv.CONTENT_ID
   AND sod.CONTENT_VERSION_ID = ccv.CONTENT_VERSION_ID;

COMMENT ON VIEW APS_CORE.STRATEGY_NOTES_FROM_CONTENT IS
    'Derived strategy/topic-facing notes from approved strategy/topic entity links or approved strategy scopes.';
